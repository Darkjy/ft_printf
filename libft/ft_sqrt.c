int		ft_sqrt(int nb)
{
	int i;
	int j;

	i = 1;
	j = 1;
	if (nb == 0)
		return (0);
	if (nb < 0)
		return (0);
	while (i * i < nb)
		i++;
	if ((nb % i) == 0)
		return (i);
	else
		return (0);
}
